package edu.aulasDio.laços;

public class iteraçãoEmArrayComFor {
    public static void main (String [] args) {
        String [] caixaStrings = {"pizza", "pão", "mortadela"};

        for (int i = 0; i < caixaStrings.length; i++) {
            System.out.println(caixaStrings[i]);
        }
    } 
}

